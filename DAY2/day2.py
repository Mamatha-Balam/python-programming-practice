# Variables example
a = "Maya"
print(a)

# Updating variable
a = "Learning Python"
print(a)


# Integer data type
my_integer = 42
print(my_integer)

# Float data type
my_float = 3.14
print(my_float)

# String data type
my_string = "Hello, World!"
print(my_string)

# Boolean data type
is_true = True
is_false = False
print(is_true)
print(is_false)


# Mixed example
name = "Maya"
age = 21
height = 5.3
is_student = True

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student:", is_student)