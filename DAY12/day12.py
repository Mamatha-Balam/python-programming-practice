# Example 1: Print numbers from 1 to 5
for num in range(1, 6):
    print(num)


# Example 2: Print numbers from 0 to 4
for num in range(5):
    print(num)


# Example 3: Print even numbers from 1 to 10
for num in range(2, 11, 2):
    print(num)


# Example 4: Iterate through a string
text = "Python"

for char in text:
    print(char)


# Example 5: Sum of numbers from 1 to 5
total = 0

for num in range(1, 6):
    total += num

print("Sum:", total)