print(a == b)   # True (comparison)
# print(a = b)  # Error (assignment not allowed in print)


# Practice Question
a = 5
b = 2 - 7   # b = -5

print(a == b)   # False
print(a != b)   # True
print(a > b)    # True
print(a < b)    # False
print(a >= b)   # True
print(a <= b)   # False